void	ft_putnbr(int nb)
{
	char	c;
	
	if (nb > 0)
	{
		c = '0' + nb % 10;
		nb = (nb - (nb % 10)) / 10;
		write(1, &c, 1);
		ft_putnbr(nb);
	}
}